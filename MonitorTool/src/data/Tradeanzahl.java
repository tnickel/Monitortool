package data;

public class Tradeanzahl
{
	private int anztrades=0;
	private int anzprofits=0;

	public Tradeanzahl()
	{}

	public int getAnztrades()
	{
		return anztrades;
	}

	public void setAnztrades(int anztrades)
	{
		this.anztrades = anztrades;
	}

	public int getAnzprofits()
	{
		return anzprofits;
	}

	public void setAnzprofits(int anzprofits)
	{
		this.anzprofits = anzprofits;
	}
	
	
	
}
